    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed

        if (mNome.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'NOME' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mNome.requestFocus();
            return;
        }

        if (mTipoDoc.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'TIPO' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mTipoDoc.requestFocus();
            return;
        }

        if (mCpf.getText().trim().replace(".", "").replace("-", "").replace("/", "").length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'CPF' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mCpf.requestFocus();
            return;
        }


        if (mCnpj.getText().trim().replace(".", "").replace("-", "").replace("/", "").length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'CNPJ' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mCnpj.requestFocus();
            return;
        }

        if (mStatus.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(null, "Campo 'STATUS' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mStatus.requestFocus();
            return;
        }

        if (mDtEntrada.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Campo 'DATA DE ENTRADA' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mDtEntrada.requestFocus();
            return;
        }

        if (mDtNasc.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Campo 'DATA DE NASCIMENTO' nào pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            mDtNasc.requestFocus();
            return;
        }

        try {
            GravarDados();
        } catch (SQLException ex) { ex.printStackTrace();}

        bNew = false;

        btIncluir.setEnabled(true);
        btExcluir.setEnabled(true);
        btGravar.setEnabled(true);
        btTras.setEnabled(true);
        btFrente.setEnabled(true);
        btIrPara.setEnabled(true);
        btImoveis.setEnabled(true);
        btSair.setEnabled(true);

    }//GEN-LAST:event_btGravarActionPerformed


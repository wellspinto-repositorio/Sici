    private void GravarDados() throws SQLException {
        SimpleDateFormat fm = new SimpleDateFormat("yyyy/MM/dd");

        String tmpDtEntrada = "0000-00-00";
        try {
            tmpDtEntrada = fm.format(mDtEntrada.getDate()).toString();
        } catch (Exception ex) {}

        String tmpDtSaida = "0000-00-00";
        try {
            tmpDtSaida = fm.format(mDtSaida.getDate()).toString();
        } catch (Exception ex) {}

        String tmpDtNasc = "0000-00-00";
        try {
            tmpDtNasc = fm.format(mDtNasc.getDate());
        } catch (Exception ex) {}

        int iNewRgPrp = 0;
        if (bNew) {
            int NewRgPrp = 0;
            try {
                NewRgPrp = Integer.parseInt(conn.LerParametros("PROP"));
            } catch (SQLException ex) {}
            int PropInc = 0;
            try {
                PropInc = Integer.parseInt(conn.LerParametros("PROPINC"));
            } catch (SQLException ex) {}
            iNewRgPrp = NewRgPrp + PropInc;

            String cPar[] = {"PROP",String.valueOf(iNewRgPrp),"NUMERICO"};
            try {
                conn.GravarParametros(cPar);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } else iNewRgPrp = Integer.parseInt(mCodigo.getText());

        if (bNew) {
            pResult.moveToInsertRow();
            pResult.updateInt("rgprp", iNewRgPrp);
        }
        
        pResult.updateString("nome", mNome.getText());
        pResult.updateString("tipoprop", mTipoDoc.getText());
        pResult.updateString("end", mEndereco.getText());
        pResult.updateString("num", mNumero.getText());
        pResult.updateString("compl", mCplto.getText());
        pResult.updateString("bairro", mBairro.getText());
        pResult.updateString("cidade",mCidade.getText());
        pResult.updateString("estado",mEstado.getText());
        pResult.updateString("cep",mCep.getText());
        pResult.updateString("tel",mTelefone.getText());
        pResult.updateString("profissao",mProfissao.getText());
        pResult.updateString("representante",mRepresentante.getText());
        pResult.updateDate("repdtnasc", (java.sql.Date) mDtNascRep.getDate());
        pResult.updateString("naciona",mNacionalidade.getText());
        pResult.updateString("ecivil", mEstCivil.getSelectedItem().toString());
        pResult.updateString("dtnasc", tmpDtNasc);
        pResult.updateString("rginsc",mIdentidade.getText());

        if (mTipoDoc.getText().contains("F")) {
            pResult.updateString("cpfcnpj",mCpf.getText());
        } else
            pResult.updateString("cpfcnpj", mCnpj.getText());
       
        pResult.updateString("conjugue",mConjugue.getText());
        pResult.updateDate("conjdtnasc", (java.sql.Date) mDtNascConj.getDate());
        pResult.updateString("banco", mBanco.getText());
        pResult.updateString("agencia", mAgencia.getText());
        pResult.updateString("conta", mCCorrente.getText());
        pResult.updateString("favorecido", mFavorecido.getText());
        pResult.updateString("email", mEmail.getText());
        pResult.updateString("dtcadastro", tmpDtEntrada);
        pResult.updateString("status", mStatus.getSelectedItem().toString());
        pResult.updateString("dtsaida", tmpDtSaida);
        pResult.updateString("msgboleta", mMSgBoleta.getText());
        pResult.updateString("msgrecibo", mMsgRecibo.getText());
        pResult.updateString("obscaixa", mObsCaixa.getText());
        pResult.updateInt("correio",((cbxEnviar.isSelected()) ? -1 : 0));
        pResult.updateString("cor_end",mEnderecoEnv.getText());
        pResult.updateString("cor_num", mNumeroEnv.getText());
        pResult.updateString("cor_compl",mCpltoEnv.getText());
        pResult.updateString("cor_bairro",mBairroEnv.getText());
        pResult.updateString("cor_cidade", mCidadeEnv.getText());
        pResult.updateString("cor_estado", mEstadoEnv.getText());
        pResult.updateString("cor_cep", mCepEnv.getText());

        if (bNew) {
            pResult.updateString("dtultpagto", "0000-00-00");
            pResult.updateFloat("vrultpagto", 0);
            pResult.updateFloat("saldoant", 0);
            
            mCodigo.setText(Integer.toString(iNewRgPrp));
            pResult.insertRow();
        } else {
            pResult.updateRow();
        }

    }
    
    public boolean MoveToProp(String campo, String seek) throws SQLException {
        boolean achei = false;
        pResult.first();
        while (pResult.next()) {
            if (pResult.getInt(campo) == Integer.parseInt(seek)) {
                achei = true;
                break;
            }
        }
        if (!achei) pResult.first();
        LerDados(false);
        return achei;
    }


    private void jComumDefaultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComumDefaultActionPerformed
        int jrow = jComum.getSelectedIndex();
        if (jrow == -1) return;
        String[] printer = VariaveisGlobais.Printer.split(",");
        String nprinter = "";
        for (int i = 0; i<printer.length;i++) {
            String[] prn = printer[i].split(":");
            if (i == jrow) {
                nprinter += prn[0] + ":" + prn[1] + ":" + prn[2] + ":*" + ",";
            } else nprinter += prn[0] + ":" + prn[1] + ":" + prn[2] + ",";

        }
        new Settings().Save("Printer",nprinter.substring(0, nprinter.length() - 1));
        VariaveisGlobais.Printer = nprinter.substring(0, nprinter.length() - 1);
        LoadThermica();
    }//GEN-LAST:event_jComumDefaultActionPerformed


    private void jUsuariosFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jUsuariosFocusLost
        String sql = "SELECT senha, atalhos FROM cadfun Where usuario='&1.'";
        sql = FuncoesGlobais.Subst(sql, new String[] {jUsuarios.getSelectedItem().toString().trim()});
        ResultSet rst = conn.AbrirTabela(sql, ResultSet.CONCUR_READ_ONLY);
        String protocolomenu = "";

        try {
            while (rst.next()) {
                jSenha.setText(rst.getString("senha"));
                protocolomenu = rst.getString("atalhos");
            }
        } catch (SQLException ex) {}

        Ler(protocolomenu);
    }//GEN-LAST:event_jUsuariosFocusLost


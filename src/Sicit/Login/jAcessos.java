    private String[] ListaUsuarios() {
        ResultSet rst = conn.AbrirTabela("SELECT usuario FROM cadfun ORDER BY f_cod;", ResultSet.CONCUR_READ_ONLY);
        String[] lista = {};
        try {
            while (rst.next()) {
                lista = FuncoesGlobais.ArrayAdd(lista, rst.getString("usuario"));
            }
        } catch (SQLException ex) {}
        DbMain.FecharTabela(rst);

        return lista;
    }


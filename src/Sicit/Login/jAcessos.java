    private void Ler(String protocolomenu) {
        Object[] mnu = ItensMenu();
        // limpar opções
        for (int i=0;i<mnu.length;i++) { ((JCheckBox) mnu[i]).setSelected(false); }

        if (!"".equals(protocolomenu.trim())) {
            String[] parte1 = protocolomenu.split(";");
            for (int i=0; i<parte1.length;i++) {
                String[] parte2 = parte1[i].split(":");
                ((JCheckBox) mnu[Integer.valueOf(parte2[0])]).setSelected(("1".equals(parte2[1]) ? true : false));
            }
        }
    }

    private String Gravar() {
        Object[] mnu = ItensMenu();
        String protocolomenu = "";
        for (int i=0;i<mnu.length;i++) {
            if (((JCheckBox) mnu[i]).isSelected()) {
                protocolomenu += i + ":1;";
            }
        }
        return protocolomenu.substring(0,protocolomenu.length() - 1);
    }

